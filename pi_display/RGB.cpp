#include "RGB.hpp"
RGB::RGB(std::string instring){
    uint32_t c;
    try{
        c=std::stol(instring, nullptr, 16);
    }
    catch (std::invalid_argument const& ex){
        // std::cout << "std::invalid_argument::what(): " << ex.what() << '\n';
        try{
            c = this->rgbMap.at(instring);
        }
        catch(const std::out_of_range& e){
            std::cerr << "Error: Color not found: " << instring << " setting to white " << e.what() << std::endl;
            c = 0xFFFFFF;
        }
        
    }
    catch (std::out_of_range const& e)
    {
        std::cerr << "std::out_of_range::what(): " << e.what() << '\n';
        // const long long ll{std::stoll(s, &pos)};
        // std::cout << "std::stoll(" << std::quoted(s) << "): " << ll
        //           << "; pos: " << pos << '\n';
    }
    catch(...){

    }

    r=c>>16&0xFF;
    g=c>>8&0xFF;
    b=c>>0&0xFF; 
}

